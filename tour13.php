          <h1 class="titre_event">Tour Paris XIII </h1>
          <p class="description_event"> La Tour Paris 13 et le street art ? C’est un événement énorme, inattendu, tenu secret depuis des mois. 
            Jamais en France un lieu n’avait réuni autant d’artistes de street art pour une exposition temporaire, éphémère à couper le souffle.
             Pendant des mois, dans le silence absolu, Mehdi, de la galerie Itinerrance (figure de proue et rassembleur incontournable de talents de street art dans la capitale) en coordination 
             avec la mairie du 13e arrondissement de Paris (et son maire Jérôme Coumet, passionné de street art) ont réuni dans une tour du front de seine (Métro Quai de la Gare) 100 artistes du monde entier (16 nationalités) 
             en leur donnant une carte blanche créative. Objectif artistique : revisitez les appartements vides de ce bâtiment qui sera démoli en fin d’année. La crème de la crème du street art s’est exprimée sur 9 étages (et le sous-sol!) 
             avec le talent qu’on leur connaît! 
